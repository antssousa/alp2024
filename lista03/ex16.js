// Solicita ao usuário as medidas das diagonais do losango
let diagonal1 = Number(prompt("Digite a medida da primeira diagonal:"))
let diagonal2 = Number(prompt("Digite a medida da segunda diagonal:"))
// Calcula a área do losango
let area = (diagonal1 * diagonal2) / 2
// Exibe o resultado
document.write(`A área do losango é ${area}`)
