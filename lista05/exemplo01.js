document.write('-----------------FOR------------------<br>')
for(let i = 1; i <= 10; i++){

    document.write(i + '<br>')
}

document.write('----------------WHILE-------------------<br>')
let j = 1
while(j <= 10){
    document.write(j + '<br>')
    j++
}

document.write('---------------DO-WHILE-------------------<br>')

let k = 1
do{
    document.write(k + '<br>')
    k++
}while(k <= 10)